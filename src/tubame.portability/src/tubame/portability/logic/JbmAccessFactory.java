package tubame.portability.logic;

/**
 * Of the file to be generated by the portable tool (search results file /
 * conversion status file)<br/>
 * Operations that access the is (write / read) Factory.<br/>
 * {@link JbmReadFacade} / {@link JbmWriteFacade} /Generate a
 * {@link ConvertWriteFacade} / {@link ConvertReadFacade}.<br/>
 * 
 * @author Copyright (C) 2013 Nippon Telegraph and Telephone Corporation,All
 *         rights reserved.
 */
public class JbmAccessFactory {

    /**
     * Default constructor.<br/>
     * It can not be instantiated from another class.<br/>
     * 
     */
    private JbmAccessFactory() {
        // no operation
    }

    /**
     * Get JbmCSV read Facade.<br/>
     * Generate {@link JbmReadFacade}.<br/>
     * 
     * @return {@link JbmReadFacade}
     */
    public static JbmReadFacade getJbmReadFacade() {
        return new JbmReadFacade();
    }

    /**
     * Get JbmCSV writing Facade.<br/>
     * Generate {@link JbmWriteFacade}.<br/>
     * 
     * @return {@link JbmReadFacade}
     */
    public static JbmWriteFacade getJbmWriteFacade() {
        return new JbmWriteFacade();
    }


}
